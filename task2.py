# Task 2: Using the Math Module for Calculations
user = int(input("Enter a number: "))

import math

SquareRoot = math.sqrt(user)
print(f"The square root of {user} is: {SquareRoot}")

Logarithm = math.log(user)
print(f"The logarithm of {user} is: {Logarithm}")

Sine = math.sin(user)
print(f"The sine of {user} is: {Sine}")
